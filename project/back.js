// Geolocation and State Finder
navigator.geolocation.getCurrentPosition(
  (position) => {
    const lat = position.coords.latitude;
    const long = position.coords.longitude;
    let foundState = "Unknown State";
    let foundDistrict = "";
    let isInIndia = false;
    console.log(lat, long);
    for (const [state, coordinates] of Object.entries(
      indianStatesCoordinates
    )) {
      if (
        Math.abs(coordinates.latitude - lat) <= 0.5 &&
        Math.abs(coordinates.longitude - long) <= 0.5
      ) {
        foundState = state.replace(/([A-Z])/g, " $1").trim(); // Format state name
        isInIndia = true; // Set flag if in India

        // Check for districts (sub-districts)
        if (coordinates.subDistricts) {
          for (const [district, subCoordinates] of Object.entries(
            coordinates.subDistricts
          )) {
            if (
              Math.abs(subCoordinates.latitude - lat) <= 0.5 &&
              Math.abs(subCoordinates.longitude - long) <= 0.5
            ) {
              foundDistrict = district; // Set district name

              break;
            }
          }
        } else {
          foundDistrict = foundState; // No sub-districts, state is the district
        }
        break;
      }
    }

    if (!isInIndia) {
      foundState = "You are not in India";
      foundDistrict = "";
    }

    if (foundDistrict) {
      span.textContent = `${foundState}, ${foundDistrict} India`;
    } else {
      span.textContent = `${foundState} India`;
    }
  },
  (error) => {
    span.textContent = `Error getting location: ${error.message}`;
  }
);
